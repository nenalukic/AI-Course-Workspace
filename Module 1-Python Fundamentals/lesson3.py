# Lesson 3: Data Structures (Lists and Dictionaries)
# A List of review scores
scores = [5, 4, 3, 5, 2]
print(f"The first score is {scores[0]}") # Output: 5

# A Dictionary representing a single product
product = {
    "id": 101,
    "name": "Mechanical Keyboard",
    "price": 120.00,
    "tags": ["tech", "gaming", "office"]
}
print(f"The product name is {product['name']}")